#include "main.h"

/**
 * main - main entry point
 *
 * description - a program that prints _putchar
 *
 * Return: returns zero
 */

int main(void)
{
	char c = '-';

	while (c <= 113)
	{
		if (c == '-')
		{
			putchar(c);
		} else if (c == 'p')
		{
			putchar(c);
		} else if (c == 'u')
		{
			putchar(c);
		} else if (c == 't')
		{
			putchar(c);
		} else if (c == 'c')
		{
			putchar(c);
		} else if (c == 'h')
		{
			putchar(c);
		} else if (c == 'a')
		{
			putchar(c);
		} else if (c == 'r')
		{
			putchar(c);
		}
	}
	putchar('\n');
	return (0);
}
