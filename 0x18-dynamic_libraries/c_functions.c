#include "func.h"

/**
 * add - A function that adds 2 numbers
 * @a: An integer input value
 * @b: Second integer input value
 *
 * Return: Integer sum of 2 values
 */

int add(int a, int b)
{
	return a + b;
}

/**
 * sub - A function that subtracts 2 numbers
 * @a: An integer input value
 * @b: Second integer input value
 *
 * Return: Integer subtraction of 2 values
 */

int sub(int a, int b)
{
        return a - b;
}

/**
 * mul - A function that mutliplies 2 numbers
 * @a: An integer input value
 * @b: Second integer input value
 *
 * Return: Integer multiplication of 2 values
 */

int mul(int a, int b)
{
        return a * b;
}

/**
 * div - A function that divides 2 numbers
 * @a: An integer input value
 * @b: Second integer input value
 *
 * Return: Integer division of 2 values
 */

int div(int a, int b)
{
        return a / b;
}

/**
 * mod - A function that finds modulus of 2 numbers
 * @a: An integer input value
 * @b: Second integer input value
 *
 * Return: Integer modulus of 2 values
 */

int mod(int a, int b)
{
        return a % b;
}
